void g_dummy_quick_setup(void) {
	g_comms_i2c_sensor_dummy.p_api->open(g_comms_i2c_sensor_dummy.p_ctrl,
			g_comms_i2c_sensor_dummy.p_cfg);
	SensorDummy_init (&g_comms_i2c_sensor_dummy);
}

void g_dummy_sensor0_read(float *p_dummy_data) {
	uint8_t dummy_data;
	SensorDummy_read(&g_comms_i2c_sensor_dummy, &dummy_data);
	*p_dummy_data = (float) dummy_data;
}
